int main(void)
{
    uint32_t screen = 0;
    u8g2_t u8g2;

    puts("Initializing SSD1306 display on I2C.");

    u8g2_Setup_ssd1306_i2c_128x64_noname_1(&u8g2, U8G2_R0,
                                           u8x8_byte_hw_i2c_riotos,
                                           u8x8_gpio_and_delay_riotos);

    u8x8_riotos_t user_data = {
        .device_index = SSD1306_I2C_DEV,
        .pin_cs = GPIO_UNDEF,
        .pin_dc = GPIO_UNDEF,
        .pin_reset = GPIO_UNDEF,
    };

    u8g2_SetUserPtr(&u8g2, &user_data);
    u8g2_SetI2CAddress(&u8g2, SSD1306_I2C_ADDR);

    u8g2_InitDisplay(&u8g2);
    u8g2_SetPowerSave(&u8g2, 0);

    puts("Drawing on screen.");

    while (1) {
        u8g2_FirstPage(&u8g2);

        do {
            u8g2_SetDrawColor(&u8g2, 1);
            u8g2_SetFont(&u8g2, u8g2_font_helvB12_tf);

            switch (screen) {
            case 0:
                u8g2_DrawStr(&u8g2, 12, 22, "Hello");
                break;
            case 1:
                u8g2_DrawStr(&u8g2, 12, 22, "RP2350!");
                break;
            case 2:
                u8g2_DrawBitmap(&u8g2, 0, 0, 8, 32, logo);
                break;
            }
        } while (u8g2_NextPage(&u8g2));

        /* show the next screen in one second */
        screen = (screen + 1) % 3;
        ztimer_sleep(ZTIMER_USEC, US_PER_SEC);
    }

    return 0;
}
