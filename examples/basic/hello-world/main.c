/**
 * @ingroup     examples
 * @{
 *
 * @file
 * @brief       SSD1306 OLED example for the RP2350 using I2C and U8g2
 *
 * Drives an SSD1306 based 128x64 OLED display connected to the first I2C
 * bus of the board. On the Raspberry Pi Pico 2 this is I2C0 with SDA on
 * GPIO4 and SCL on GPIO5, the display is expected at address 0x3c.
 *
 * @author      Tom Hert <git@annsann.eu>
 *
 * @}
 */

#include <stdio.h>

#include "periph/i2c.h"
