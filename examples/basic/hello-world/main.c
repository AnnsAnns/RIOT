#include "u8x8_riotos.h"

/** I2C bus the display is connected to */
#ifndef SSD1306_I2C_DEV
#define SSD1306_I2C_DEV 0
#endif

/** I2C address of the display */
#ifndef SSD1306_I2C_ADDR
#define SSD1306_I2C_ADDR 0x3c
#endif

